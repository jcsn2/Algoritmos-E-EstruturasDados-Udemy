import math

#Localizar menor inteiro maior ou igual a um determinado valor
print(math.ceil(3.14))

#Localizar valor absoluto de um numero
print(math.fabs(4))

#Localizar maior inteiro menor ou igual a um determinado valor
print(math.floor(3.14))

#resultado de exp
print(math.exp(2))

#Log base 10
print(math.log10(100))

#Localizar o resultado de numero elevado a outro
print(math.pow(2,10))

#Localizar raiz quadrada
print(math.sqrt(100))

#Cosseno
print(math.sin(30))

#Seno
print(math.cos(20))

