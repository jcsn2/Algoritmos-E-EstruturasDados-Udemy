matriz = [
	['joão', 8, 7, 6],
	['pedro', 4, 9, 10],
	['marcos', 6, 6, 8],
]

for linha in matriz:
	for col in linha:
		print(str(col) + '\t', end = ' ')
	print('')

'''
Estrutura de dados para quando se tem muitas variáveis do mesmo tipo.
Vetor e uma Matriz unidimensional.
Matriz é um vetor de vetores (multidimensional).
Um vetor possui apenas uma linha e várias colunas.
'''